package xmladapter;

/**
 *
 * @author fairfax
 */
public class TaskXMLAdapterException extends Exception {
    private String taskName;
    
    public TaskXMLAdapterException(String message, String task) {
        super(message);
        taskName = task;
    }
    
    public String getTaskName() {
        return taskName;
    }
}
